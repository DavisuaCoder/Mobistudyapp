export default `Mobistudy es una iniciativa abierta que permite a los ciudadanos contribuir 
a la investigación clínica con sus datos. Los participantes descargan una app para teléfonos inteligentes 
y, tras registrarse y cumplimentar un breve perfil, se les proponen un/os
estudio/s de investigación. Si encuentran un estudio que les interesa, están 
guiados a través de un proceso de consentimiento informado fácil de entender. Tras la aceptación, 
a los participantes se les proponen tareas, como responder cuestionarios, 
realizar ejercicios sencillos o proporcionar datos recopilados por teléfonos móviles, 
dispositivos portátiles o sensores médicos. <br> <br>
Los datos recopilados de esta manera se mantienen seguros dentro de los servidores de Mobistudy 
y se comparten solo con los investigadores que involucrados en el estudio`
